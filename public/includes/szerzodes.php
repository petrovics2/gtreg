<p>Jelen Általános Szerződési Feltételek a 2018. augusztus 21-25. között megrendezett „Közgáz Gólyatábor 2018” elnevezésű rendezvény regisztrációját és menetét taglalja. A „Közgáz Gólyatábor 2018” a Budapesti Corvinus Egyetem (továbbiakban: BCE) megbízásából az Öntevékeny Csoportok Irodája (továbbiakban: ÖCSI) által kerül megrendezésre. A rendezvény felelős személye: Sümegi Bence, Ügyvezető Elnök (sumegi.bence@bceocsi.com, +36704233263).</p>

