<h5 class="font-weight-bold">II. A regisztráció részletes menete:</h5>
<p>(1), A gólyák a regisztrációt a regisztracio.bceocsi.com weboldalon indítják, ahol megadják a szerződés I./(1.) pontjában feltűntetett adatokat, elfogadják a szerződési feltételeket és a gólyatábor házirendjét.</p>
<p>(2), A regisztráció ezután a I./(2/b) pontban ismertetett procedúrán keresztül folytatódik, azzal, hogy a főszervező az egyetem által kijelölt személlyel egyezteti a regisztráló hallgatói státuszát.</p>
<p>(3), 24-48 órán belül ezután a hallgató kap egy kódot, melyet fel kell tüntetnie a fizetés során. A fizetésnek pontosan 25.000 forintnak kell lennie, függetlenül az étkezés és az ottlét tartamától. A hallgatók jogosultak visszaigényelni a különbözetet, amennyiben korábban távoznak és/vagy később érkeznek, ezt a regisztrációs hét (szeptember 3-i hét) során tehetik meg, a Budapesti Corvinus Egyetem földszinti 11-es irodában (ÖCSI irodában).</p>